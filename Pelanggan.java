public class Pelanggan {
    private String nama;
    private String alamat;
    private String nomorTelpon;
    private String email;

    public Pelanggan () {
        this.nama = "";
        this.alamat = "";
        this.nomorTelpon = "";
        this.email = "";
    }

 
    public Pelanggan (String nama, String alamat, String nomorTelpon, String email) {
        this.nama = nama;
        this.alamat = alamat;
        this.nomorTelpon = nomorTelpon;
        this.email = email;
    }

    

}